void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART1)
	{
		if(Rx_Data != 46 )
		{
			Rx_Buff[_rxIndex++] = Rx_Data;
		}
		else if (Rx_Data == 46 )
		{
			_rxIndex = 0;
		Tx_Flag = 1;
		}
		HAL_UART_Receive_IT(&huart1, &Rx_Data,1);
	}
}


void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{

//kiem tra kenh gay ra ngat
   if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1) 
   {
		 demxungHall++;
// kiem tra xem co phai lan lay mau dau tien
       if (Is_First_Captured==0) 
       {
// luu gia tri thoi gian cua lan lay mau dau
           IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); 
           Is_First_Captured = 1; 
       }
// kiem tra xem co phai lan lay mau thu 2
       else if (Is_First_Captured==1)  
       {
// luu gia tri thoi gian cua lan lay mau thu 2
           IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); 
           __HAL_TIM_SET_COUNTER(htim, 0); // reset the counter
           if(IC_Val2 > IC_Val1)
           {
// Thoi gian giua 2 lan xung len
						 Difference = IC_Val2-IC_Val1;
						 float refClock = TIMCLOCK/(PRESCALAR);
						 tocdo_thuc = 60*(refClock/Difference)/2;
					 }
					 Is_First_Captured = 0;
       }
   }
}

int PID( int Output, float frequency_ref,float tocdo_thuc)
{
error = frequency_ref - tocdo_thuc;
errorSum += error*Timechange;
outpid = kp*error + ki*errorSum + (error-error1)*kd/(Timechange);
if(Output+ outpid >=99)
{
Output = 99;
}
else if(Output +outpid <0)
{
Output =0;
}
else
{
Output = Output + outpid;
}
error1=error;
return Output;
}


